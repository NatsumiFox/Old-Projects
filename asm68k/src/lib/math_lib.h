#if !defined(INCLUDE_LIB_MATH_H)
#define INCLUDE_LIB_MATH_H

/**
	\brief Macro that takes in 2 values and returns the one that is larger
*/
#define MAX(x, y) (((x) > (y)) ? (x) : (y))

/**
	\brief Macro that takes in 2 values and returns the one that is smaller
*/
#define MIN(x, y) (((x) < (y)) ? (x) : (y))

#endif // INCLUDE_LIB_MATH_H
